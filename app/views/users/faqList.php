            <ul>
                <li>Mengeluarkan padi dan beras untuk menampung keperluan beras negara.</li>
                <li>Mengeluarkan bahan makanan lain untuk memenuhi permintaan tempatan dan keperluan negara.</li>
                <li>Meningkatkan pendapatan per kapita keluargatani selaras dengan pendapatan per kapita negara.</li>
                <li>Menggalakkan dan memajukan industri hiliran berasaskan pertanian.</li>
                <li>Membangun dan memperteguhkan institusi peladang untuk menaungi keluarga tani.</li>
            </ul>
